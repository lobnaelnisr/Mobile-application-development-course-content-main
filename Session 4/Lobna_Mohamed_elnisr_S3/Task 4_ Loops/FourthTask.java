public class FourthTask {
    public static void main(String[] args) {
        int sum = 0;
        int limit = 10;

        // Loop to compute the sum of the first 10 natural numbers
        for (int i = 1; i <= limit; i++) {
            sum += i;
        }

        // Display the result
        System.out.println("The sum of the first " + limit + " natural numbers is: " + sum);
    }

}
